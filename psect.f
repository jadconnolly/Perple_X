      subroutine getxy (i,j,x,y,x1,y1,x2,y2)

c getxy - get coordinates for grid node volume

      implicit none

      include 'perplex_parameters.h'

      integer i,j

      double precision x,y,x1,x2,y1,y2,dx,dy

      integer jvar
      double precision var,dvr,vmn,vmx
