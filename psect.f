         y2 = y + 0.5d0*dy
         goto 99

      end if 
c                                 on an edge:
      if (j.eq.loopy) then 
         y2 = y
         y1 = y - 0.5d0*dy
      else if (j.eq.1) then 
         y1 = y
         y2 = y + 0.5d0*dy
      else 
         y1 = y - 0.5d0*dy
         y2 = y + 0.5d0*dy
      end if 

      if (loopx.eq.1) then 
         x1 = x
         x2 = x + 0.5d0*dx
      else if (i.eq.loopx) then 
         x1 = x - dx*0.5d0
         x2 = x
      else if (i.eq.1) then 
         x1 = x
         x2 = x + 0.5d0*dx
      else 
         x1 = x - 0.5d0*dx
